public class Marca {
    private int id_mar;
    private String Nombre_mar;
    private int cod_eli;

    public int getId_mar() {
        return id_mar;
    }

    public void setId_mar(int id_mar) {
        this.id_mar = id_mar;
    }

    public String getNombre_mar() {
        return Nombre_mar;
    }

    public void setNombre_mar(String Nombre_mar) {
        this.Nombre_mar = Nombre_mar;
    }

    public int getCod_eli() {
        return cod_eli;
    }

    public void setCod_eli(int cod_eli) {
        this.cod_eli = cod_eli;
    }
    
    
}
