    private void iniDatosEscritorio(){
        Integer cantVentas=(Integer)LoginFrame.sesionUsuario.get("cantVenta");
        lblMostrarVentasUsuario.setText(cantVentas.toString());
        
        response=escritorioController.TotalProveedores();
        lblMostrarProveedores.setText(response.get("Cantidad").toString());
        
        response=escritorioController.TotalIngresos();
        lblMostrarIngresos.setText(response.get("Total").toString());
        
        response=escritorioController.TotalArticulos();
        lblMostrarArticulos.setText(response.get("Cantidad").toString());
    }
