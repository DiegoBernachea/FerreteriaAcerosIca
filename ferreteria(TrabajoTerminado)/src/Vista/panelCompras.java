    private void Registrar(){
        String proveedor=cbxproveedor.getSelectedItem().toString();
        int usuario= Integer.parseInt(LoginFrame.sesionUsuario.get("id").toString());
        String fecha= txtfecha.getText();
        float total= Float.parseFloat(txttotal.getText());
        
        int idCompra=compraController.registrarCompra(proveedor,usuario, fecha, total);
        if(idCompra>1){

            DefaultTableModel model = (DefaultTableModel) tblDetalleCompra.getModel();
            int rowCount = model.getRowCount();

            for (int i = 0; i < rowCount; i++) {
                String articulo = (String) model.getValueAt(i, 0);
                int cantidad = Integer.parseInt(model.getValueAt(i, 2).toString());
                float subtotal = Float.parseFloat(model.getValueAt(i, 3).toString());

                compraController.registrarDetallecompra(idCompra, articulo, cantidad, subtotal);
            }
            iniTablaCompras();
            ModalNotificar modal = new ModalNotificar("Registrado con exito");
            JDialog dialogo = new JDialog(modal, "Mi di√°logo", true); // true indica que es modal
            dialogo.setContentPane(modal.getContentPane()); // agregamos el contenido del JFrame al JDialog
            dialogo.setSize(new Dimension(400, 200)); // ajustamos el tama√±o del JDialog al del JFrame
            dialogo.setUndecorated(true);
            dialogo.setLocationRelativeTo(null); // centra el dialogo en la pantalla
            dialogo.setVisible(true);
        }else{
            ModalError modal = new ModalError("ocurrio un error");
            JDialog dialogo = new JDialog(modal, "Mi di√°logo", true); // true indica que es modal
            dialogo.setContentPane(modal.getContentPane()); // agregamos el contenido del JFrame al JDialog
            dialogo.setSize(new Dimension(400, 200)); // ajustamos el tama√±o del JDialog al del JFrame
            dialogo.setUndecorated(true);
            dialogo.setLocationRelativeTo(null); // centra el dialogo en la pantalla
            dialogo.setVisible(true);
        }      
    }
