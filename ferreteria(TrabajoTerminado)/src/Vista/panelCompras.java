    private void tblComprasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblComprasMouseClicked
        // TODO add your handling code here:
        TableModel model = tblCompras.getModel();

        // Verifica si hay una fila seleccionada
        int selectedRow = tblCompras.getSelectedRow();
        
            
        // Obtiene los valores de las columnas de la fila seleccionada
        Object value1 = model.getValueAt(selectedRow, 0); // Valor de la primera columna
        Object value2 = model.getValueAt(selectedRow, 1); // Valor de la primera columna
        Object value3 = model.getValueAt(selectedRow, 2); // Valor de la primera columna
        Object value4 = model.getValueAt(selectedRow, 3); // Valor de la primera columna
        Object value5 = model.getValueAt(selectedRow, 4); // Valor de la primera columna
        
        
        int idCompra = Integer.parseInt(value1.toString());
        DefaultTableModel modelDeta = new DefaultTableModel();
        modelDeta.addColumn("Articulo");
        modelDeta.addColumn("Descripcion");
        modelDeta.addColumn("Cantidad");
        modelDeta.addColumn("SubTotal");
        
        Map<String, Object>[] detalle=compraController.ObtenerDetallePorIdCompra(idCompra);
        for(Map<String, Object> res : detalle){
            Map<String, Object>[] articulodeta=compraController.ObtenerArticuloPorId((Integer)res.get("idArticulo"));
            modelDeta.addRow(new Object[]{articulodeta[0].get("nombre"),articulodeta[0].get("descripcion"), res.get("Cantidad"),res.get("subTotal")});        
        } 
        
        
        tblModalDetCompra.setModel(modelDeta);

        txtModalProveedor.setText(value2.toString());
        txtModalUsuario.setText(value3.toString());
        txtModalFecha.setText(value4.toString());
        txtModalTotal.setText(value5.toString());
           
        ModalDetalleCompra();
        dialog.setLocationRelativeTo(null); // centra el dialogo en la pantalla
        dialog.setVisible(true);

        
    }//GEN-LAST:event_tblComprasMouseClicked

    private void btnAgregarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAgregarMouseClicked
        // TODO add your handling code here:
        int rowCount = tblDetalleCompra.getRowCount();
                int columnCount = tblDetalleCompra.getColumnCount();
                Object[][] datosExistentes = new Object[rowCount][columnCount];
                for (int i = 0; i < rowCount; i++) {
                    for (int j = 0; j < columnCount; j++) {
                        datosExistentes[i][j] = tblDetalleCompra.getValueAt(i, j);
                    }
                }

                // Crear un nuevo modelo basado en los valores existentes
                String[] columnas = new String[columnCount];
                for (int i = 0; i < columnCount; i++) {
                    columnas[i] = tblDetalleCompra.getColumnName(i);
                }
               DefaultTableModel modelo = new DefaultTableModel(datosExistentes, columnas);
               
               String ArticuloNom=cbxarticulo.getSelectedItem().toString();
               Map<String, Object>[] selecArticulo=compraController.ObtenerArticuloPorNombre(ArticuloNom);
               String ArticuloDescrip=selecArticulo[0].get("descripcion").toString();
               String cantidad= txtcantidad.getText();
               String subTotal= txtSubTotal.getText();
               
                // Agregar una nueva fila al modelo de tabla
                
                
                float total=Float.parseFloat(txttotal.getText());
                float sub=Float.parseFloat(subTotal);
                total=sub+total;
                DecimalFormatSymbols separadores = new DecimalFormatSymbols(Locale.getDefault());
                separadores.setDecimalSeparator('.');
                DecimalFormat formato = new DecimalFormat("#." + "0".repeat(2), separadores);
                String redondeado = formato.format(total);
                txttotal.setText(redondeado);
                
                modelo.addRow(new Object[]{ArticuloNom, ArticuloDescrip,cantidad,subTotal});
                // Establecer el nuevo modelo en la tabla
        tblDetalleCompra.setModel(modelo);
        txtcantidad.setText("");
        txtSubTotal.setText("");
    }//GEN-LAST:event_btnAgregarMouseClicked

