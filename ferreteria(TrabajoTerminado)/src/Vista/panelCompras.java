    private void btnBuscarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnBuscarMouseClicked
        // TODO add your handling code here:
        iniTablaCompras();
        
        String elementoBusqueda = txtBusqueda.getText(); // Elemento que se desea buscar
        if(!elementoBusqueda.equals("")){
            DefaultTableModel model = (DefaultTableModel) tblCompras.getModel();
            int rowCount = model.getRowCount();
            int columnCount = model.getColumnCount();

            List<Integer> filasCoincidentes = new ArrayList<>();

            for (int row = 0; row < rowCount; row++) {
                for (int column = 0; column < columnCount; column++) {
                    Object cellValue = model.getValueAt(row, column);
                    String cellString = String.valueOf(cellValue);

                    if (cellString.equals(elementoBusqueda)) {
                        filasCoincidentes.add(row);
                        // Puedes realizar alguna acci√≥n adicional, como resaltar la fila
                    }
                }
            }
            DefaultTableModel modelCompra = new DefaultTableModel() {
                @Override
                public boolean isCellEditable(int row, int column) {
                    return false;
                }
            };
            modelCompra.addColumn("ID");
            modelCompra.addColumn("Proveedor");
            modelCompra.addColumn("Usuario");
            modelCompra.addColumn("Fecha");
            modelCompra.addColumn("Total");

            for (Integer filaCoincidente : filasCoincidentes) {
                Object[] rowData = new Object[5];
                rowData[0] = tblCompras.getValueAt(filaCoincidente, 0); 
                rowData[1] = tblCompras.getValueAt(filaCoincidente, 1);  
                rowData[2] = tblCompras.getValueAt(filaCoincidente, 2); 
                rowData[3] = tblCompras.getValueAt(filaCoincidente, 3);  
                rowData[4] = tblCompras.getValueAt(filaCoincidente, 4);  
               
                modelCompra.addRow(rowData);
            }
      
            tblCompras.setModel(modelCompra);
            tblCompras.repaint();
        }else{
            iniTablaCompras();
        }
        
        
        
    }//GEN-LAST:event_btnBuscarMouseClicked

