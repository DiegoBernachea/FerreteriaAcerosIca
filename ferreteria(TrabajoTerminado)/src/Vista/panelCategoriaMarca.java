    private void btnBuscarcatMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnBuscarcatMouseClicked
        // TODO add your handling code here:
        iniTablaCategoria();
        String elementoBusqueda = txtBusquedacat.getText(); // Elemento que se desea buscar
        if(!elementoBusqueda.equals("")){
            DefaultTableModel model = (DefaultTableModel) tblCategoria.getModel();
            int rowCount = model.getRowCount();
            int columnCount = model.getColumnCount();

            List<Integer> filasCoincidentes = new ArrayList<>();

            for (int row = 0; row < rowCount; row++) {
                for (int column = 0; column < columnCount; column++) {
                    Object cellValue = model.getValueAt(row, column);
                    String cellString = String.valueOf(cellValue);

                    if (cellString.equals(elementoBusqueda)) {
                        filasCoincidentes.add(row);
                        // Puedes realizar alguna acci√≥n adicional, como resaltar la fila
                    }
                }
            }
            DefaultTableModel modelCategoria = new DefaultTableModel() {
                @Override
                public boolean isCellEditable(int row, int column) {
                    return false;
                }
            };
            modelCategoria.addColumn("id");
            modelCategoria.addColumn("Nombre");

            for (Integer filaCoincidente : filasCoincidentes) {
                Object[] rowData = new Object[5];
                rowData[0] = tblCategoria.getValueAt(filaCoincidente, 0);  // id_almacen
                rowData[1] = tblCategoria.getValueAt(filaCoincidente, 1);  // id_articulo
                modelCategoria.addRow(rowData);
            }
      
            tblCategoria.setModel(modelCategoria);
            tblCategoria.repaint();
        }else{
            iniTablaCategoria();
        }
    }//GEN-LAST:event_btnBuscarcatMouseClicked

