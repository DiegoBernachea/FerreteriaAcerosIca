    private void iniTablaCategoria(){
        tablaCategoria = catmarController.ObtenerCategorias();
        DefaultTableModel model = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        model.addColumn("id");
        model.addColumn("Nombre");

        for(Map<String, Object> res : tablaCategoria){
            model.addRow(new Object[]{res.get("id"), res.get("nombre")});        
        }
        
        tblCategoria.setModel(model);
        tblCategoria.repaint();
    }
