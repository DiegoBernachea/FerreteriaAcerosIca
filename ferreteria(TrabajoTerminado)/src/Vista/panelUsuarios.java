    private void btnBuscarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnBuscarMouseClicked
        // TODO add your handling code here:
        iniTablaUsuarios();
       String elementoBusqueda = txtBusqueda.getText(); // Elemento que se desea buscar
        if(!elementoBusqueda.equals("")){
            DefaultTableModel model = (DefaultTableModel) tblUsuarios.getModel();
            int rowCount = model.getRowCount();
            int columnCount = model.getColumnCount();

            List<Integer> filasCoincidentes = new ArrayList<>();

            for (int row = 0; row < rowCount; row++) {
                for (int column = 0; column < columnCount; column++) {
                    Object cellValue = model.getValueAt(row, column);
                    String cellString = String.valueOf(cellValue);

                    if (cellString.equals(elementoBusqueda)) {
                        filasCoincidentes.add(row);
                        // Puedes realizar alguna acci√≥n adicional, como resaltar la fila
                    }
                }
            }
            DefaultTableModel modelUsuario = new DefaultTableModel() {
                @Override
                public boolean isCellEditable(int row, int column) {
                    return false;
                }
            };
            modelUsuario.addColumn("Id");
            modelUsuario.addColumn("Usuario");
            modelUsuario.addColumn("Nombre");
            modelUsuario.addColumn("Contrase√±a");
            modelUsuario.addColumn("Rol");
            modelUsuario.addColumn("Cantidad de Ventas");

            for (Integer filaCoincidente : filasCoincidentes) {
                Object[] rowData = new Object[6];
                rowData[0] = tblUsuarios.getValueAt(filaCoincidente, 0);  
                rowData[1] = tblUsuarios.getValueAt(filaCoincidente, 1);  
                rowData[2] = tblUsuarios.getValueAt(filaCoincidente, 2);  
                rowData[3] = tblUsuarios.getValueAt(filaCoincidente, 3);  
                rowData[4] = tblUsuarios.getValueAt(filaCoincidente, 4);  
                rowData[5] = tblUsuarios.getValueAt(filaCoincidente, 5); 
 
                modelUsuario.addRow(rowData);
            }
      
            tblUsuarios.setModel(modelUsuario);
            tblUsuarios.repaint();
        }else{
            iniTablaUsuarios();
        }
    }//GEN-LAST:event_btnBuscarMouseClicked

