    private void btnEditarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEditarMouseClicked
        // TODO add your handling code here:
        TableModel model = tblUsuarios.getModel();

        // Verifica si hay una fila seleccionada
        int selectedRow = tblUsuarios.getSelectedRow();
        
        if (selectedRow == -1) {
            ModalError modal = new ModalError("No hay fila seleccionada");
            JDialog dialogo = new JDialog(modal, "Mi di√°logo", true); // true indica que es modal
            dialogo.setContentPane(modal.getContentPane()); // agregamos el contenido del JFrame al JDialog
            dialogo.setSize(new Dimension(400, 200)); // ajustamos el tama√±o del JDialog al del JFrame
            dialogo.setUndecorated(true);
            dialogo.setLocationRelativeTo(null); // centra el dialogo en la pantalla
            dialogo.setVisible(true);
        }
        else{
            
            // Obtiene los valores de las columnas de la fila seleccionada
            Object value1 = model.getValueAt(selectedRow, 0); // Valor de la primera columna
            Object value2 = model.getValueAt(selectedRow, 1); // Valor de la primera columna
            Object value3 = model.getValueAt(selectedRow, 2); // Valor de la primera columna
            Object value4 = model.getValueAt(selectedRow, 3); // Valor de la primera columna
            Object value5 = model.getValueAt(selectedRow, 4); // Valor de la primera columna
            
            int idUsuario = Integer.parseInt(value1.toString());
            
            txtUsuario.setText(value2.toString());
            txtnombre.setText(value3.toString());
            txtClave.setText(value4.toString());
            for (int i = 0; i < cbxRol.getItemCount(); i++) {
                String elemento = cbxRol.getItemAt(i);
                if (elemento.equals(value5.toString())) {
                    // Se encontr√≥ el elemento con el nombre deseado, se selecciona
                    cbxRol.setSelectedIndex(i);
                    break;
                }
            }
            
            idSeleccionado=idUsuario;
            editNuevo=false;
            EditarModalUsuarios();
            dialog.setLocationRelativeTo(null); // centra el dialogo en la pantalla
            dialog.setVisible(true);
            
        }
    }//GEN-LAST:event_btnEditarMouseClicked

