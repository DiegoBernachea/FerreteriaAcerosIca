    private void btnBuscarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnBuscarMouseClicked
        // TODO add your handling code here:
        iniTablaArticulos();
        String elementoBusqueda = txtBusqueda.getText(); // Elemento que se desea buscar
        if(!elementoBusqueda.equals("")){
            DefaultTableModel model = (DefaultTableModel) tblArticulos.getModel();
            int rowCount = model.getRowCount();
            int columnCount = model.getColumnCount();

            List<Integer> filasCoincidentes = new ArrayList<>();

            for (int row = 0; row < rowCount; row++) {
                for (int column = 0; column < columnCount; column++) {
                    Object cellValue = model.getValueAt(row, column);
                    String cellString = String.valueOf(cellValue);

                    if (cellString.equals(elementoBusqueda)) {
                        filasCoincidentes.add(row);
                        // Puedes realizar alguna acci√≥n adicional, como resaltar la fila
                    }
                }
            }
            DefaultTableModel modelArticulo = new DefaultTableModel() {
                @Override
                public boolean isCellEditable(int row, int column) {
                    return false;
                }
            };
            modelArticulo.addColumn("id");
            modelArticulo.addColumn("Nombre");
            modelArticulo.addColumn("Descripcion");
            modelArticulo.addColumn("Categoria");
            modelArticulo.addColumn("Marca");

            for (Integer filaCoincidente : filasCoincidentes) {
                Object[] rowData = new Object[5];
                rowData[0] = tblArticulos.getValueAt(filaCoincidente, 0);  // id_almacen
                rowData[1] = tblArticulos.getValueAt(filaCoincidente, 1);  // id_articulo
                rowData[2] = tblArticulos.getValueAt(filaCoincidente, 2);  // stock
                rowData[3] = tblArticulos.getValueAt(filaCoincidente, 3);  // precio_unitario
                rowData[4] = tblArticulos.getValueAt(filaCoincidente, 4);  // precio_unitario
                modelArticulo.addRow(rowData);
            }
      
            tblArticulos.setModel(modelArticulo);
            tblArticulos.repaint();
        }else{
            iniTablaArticulos();
        }
    }//GEN-LAST:event_btnBuscarMouseClicked

